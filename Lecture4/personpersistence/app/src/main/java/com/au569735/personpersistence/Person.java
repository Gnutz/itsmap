package com.au569735.personpersistence;

public class Person {

    public String FirstName;
    public String LastName;
    public String Age;
    public String PhoneNumber;

    public Person(String firstName, String lastName, String age, String phoneNumber) {
        FirstName = firstName;
        LastName = lastName;
        Age = age;
        PhoneNumber = phoneNumber;
    }
}
