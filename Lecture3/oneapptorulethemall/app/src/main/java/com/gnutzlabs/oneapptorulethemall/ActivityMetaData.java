package com.gnutzlabs.oneapptorulethemall;

public class ActivityMetaData {
    public int Id;
    public int IconId;
    public String Name;
    public String Description;

    public ActivityMetaData(int id, int iconId, String name, String description){
        Id = id;
        IconId = iconId;
        Name = name;
        Description = description;
    }
}
